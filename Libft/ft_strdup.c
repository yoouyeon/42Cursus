#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	i;
	char	*temp;

	if (!(temp = (char*)malloc(ft_strlen(s1) + 1)))
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		temp[i] = s1[i];
		i++;
	}
	temp[i] = '\0';
	return (temp);
}
